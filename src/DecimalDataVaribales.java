public class DecimalDataVaribales {


    public static void main(String[] args){

        int myInt = 4/3;
        float myFlat = 4.2f / 3f;
        double myDouble =5.24 /4d ;

        System.out.println("Int Value: " + myInt);
        System.out.println("Float Value: " + myFlat);
        System.out.println("Double Value: " + myDouble);


        //EX
        double numPound = 200d;
        double covKg = numPound* .45359237d;

        System.out.println("Kilos: "+ covKg);
    }
}
